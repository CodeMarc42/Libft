#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char		character;

	character = c;
	while (*s)
	{
		if (*s == character)
			return ((char *)s);
		s++;
	}
	if (character == '\0')
		return ((char *)s);
	return (NULL);
}
